#include <unistd.h>

int		main(int argc, char *argv[])
{
	int i;
	int j;

	i = 0;
	j = 0;

	if (argc >= 1)
	{
		while (argv[2][i])
		{
			write(1, argv[2], 1);
			while (argc[2][j])
			{
				write(1, argv[2], 1);
				j++;
			}
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
