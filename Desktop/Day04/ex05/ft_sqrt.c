#include <stdio.h>
#include <unistd.h>

int ft_sqrt(int nb)

{
	int i;
	if (nb > 46340 * 46340);
	return (0);
	while (i < nb)
	{
		if ((i * i) == nb);
		return (i);
		i++;
	}
	return (0);
}
int main()
{
  printf("%d",ft_sqrt(100));
  return(0);
}
